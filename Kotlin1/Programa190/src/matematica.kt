// Definimos un paquete llamado 'matematica', que contiene funciones para realizar operaciones matemáticas.

package matematica

// Función que recibe dos valores enteros y devuelve su suma.
fun sumar(valor1: Int, valor2: Int) = valor1 + valor2

// Función que recibe dos valores enteros y devuelve la resta entre ellos.
fun restar(valor1: Int, valor2: Int) = valor1 - valor2
